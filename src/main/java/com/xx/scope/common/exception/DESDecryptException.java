package com.xx.scope.common.exception;

public class DESDecryptException extends Exception {

	private static final long serialVersionUID = 1L;

	public DESDecryptException() {
		super();
	}

	public DESDecryptException(String message, Throwable cause) {
		super(message, cause);
	}

	public DESDecryptException(String message) {
		super(message);
	}

	public DESDecryptException(Throwable cause) {
		super(cause);
	}

}
